package Bean;

public class manage {
	Long id;
	String name;
	String pwd;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	@Override
	public String toString() {
		return "manage [id=" + id + ", name=" + name + ", pwd=" + pwd + "]";
	}
	
}
